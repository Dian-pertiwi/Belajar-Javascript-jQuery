function tambah(p1, p2) {
  let tambah = p1 + p2;
  return tambah;
}
function kurang(p1, p2) {
  let kurang = p1 - p2;
  return kurang;
}
function kali(p1, p2) {
  var kali = p1 * p2;
  return kali;
}
function bagi(p1, p2) {
  var bagi = p1 / p2;
  return bagi;
}

console.log(tambah(6, 3));
console.log(kurang(6, 3));
console.log(kali(6, 3));
console.log(bagi(6, 3));
